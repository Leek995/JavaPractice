package Section4;

public class DoublesDataTypes {
    public static void main(String[] args){
        // int data type is a primitive data type
        // 8 data types in java
        // byte, short, int, long, float, double, boolean, char
        // byte, short, int, long are integer types bc they can potentially hold each other
        int guitar = 10;
        double x  = 3;
        System.out.println(guitar - x);

    }
}
